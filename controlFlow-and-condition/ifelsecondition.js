let marks = prompt("Please Enter Your Marks");


// let marks = Number(mark)

if(marks > 100){
    alert("Please enter valid digits");
}

else if(marks > 90){
    alert("Congratulations, your grade is A+");
}

else if(marks > 80){
    alert("Congratulations, your grade is A")
}

else if(marks > 70){
    alert("Congratulations, your grade is B")
}

else if(marks > 33){
    alert("Your grade is C")
}

else{
    alert("You are fail")
}


